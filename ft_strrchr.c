char	*ft_strrchr(const char *s, int c)
{
	int	n;
	int	i;

	n = 0;
	while (s[n])
	{
		if (s[n] == c)
			i = n;
		n++;
	}
	if (!(s[n]) && i != 0)
		return (&s[i]);
	if (c == '\0')
	{
		i = n;
		return (&s[i]);
	}
	return (0);
}
