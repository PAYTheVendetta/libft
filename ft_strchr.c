char	*ft_strchr(const char *s, int c)
{
	int	n;

	n = 0;
	while (s[n])
	{
		if (s[n] == c)
			return (&s[n]);
		n++;
	}
	if (c == '\0')
		return (&s[n]);
	return (0);
}
